package package_javabasics;

public class MultipleIFblockStatements {

	public static void main(String[] args) 
	{
		int a=10;
		int b=20;
		int c=30;
		if(a<b)
		{
			System.out.println("1");
		}
		if(a<c)
		{
			System.out.println("2");
		}
		if(b<c)
		{
			System.out.println("3");
		}
		if(c>a)
		{
			System.out.println("4");
		}
		
	}

}
