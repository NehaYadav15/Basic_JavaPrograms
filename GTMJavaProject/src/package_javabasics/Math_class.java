package package_javabasics;

public class Math_class {

	public static void main(String[] args) 
	{
		System.out.println(Math.addExact(20, 30));
		System.out.println(Math.addExact(677856, 23455678));
		System.out.println(Math.subtractExact( 899, 567));
		System.out.println(Math.subtractExact(3467886, 56786));
		System.out.println(Math.multiplyExact(987, 886));
		System.out.println(Math.abs(567.64367889));
		System.out.println(Math.abs(-77654));
		System.out.println(Math.max(876.76544322, 665.976543234));
		System.out.println(Math.min(65.8877654, 87.765543));
		
		System.out.println(Math.random());

	}

}
